package com.shggc.project.fwjk.dtxx.domain;

import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * 
 * @author 
 * 房产信息类，为组织json数据创建
 *
 */
public class FcProperties {
	//房产编号,对应t_jc_fc中fcbh.
	//为符合json数据格式，命名为guid
	@JsonProperty("GUID")
	private String guid;
	@JsonProperty("房屋座落")
	private String fwzl;
	@JsonProperty("产权人")
	private String cqr;
	@JsonProperty("房屋产权证")
	private String fwcqz;
	@JsonProperty("房屋产别")
	private String fwcb;
	@JsonProperty("房屋间数")
	private Double fwjsh;
	@JsonProperty("建筑面积")
	private Double jzmjh;
	@JsonProperty("土地使用证号")
	private String tdsyzh;
	@JsonProperty("土地使用面积")
	private Double tdsymj;
	@JsonProperty("权利类型")
	private String qllx;
	@JsonProperty("权利性质")
	private String qlxz;
	@JsonProperty("备注")
	private String bz;
	
	
	
	public FcProperties(String guid, String fwzl, String cqr, String fwcqz, String fwcb, Double fwjsh, Double jzmjh,
			String tdsyzh, Double tdsymj, String qllx, String qlxz, String bz) {
		super();
		this.guid = guid;
		this.fwzl = fwzl;
		this.cqr = cqr;
		this.fwcqz = fwcqz;
		this.fwcb = fwcb;
		this.fwjsh = fwjsh;
		this.jzmjh = jzmjh;
		this.tdsyzh = tdsyzh;
		this.tdsymj = tdsymj;
		this.qllx = qllx;
		this.qlxz = qlxz;
		this.bz = bz;
	}

	public String getGuid() {
		return guid;
	}

	public void setGuid(String guid) {
		this.guid = guid;
	}

	public String getFwzl() {
		return fwzl;
	}

	public void setFwzl(String fwzl) {
		this.fwzl = fwzl;
	}

	public String getCqr() {
		return cqr;
	}

	public void setCqr(String cqr) {
		this.cqr = cqr;
	}

	public String getFwcqz() {
		return fwcqz;
	}

	public void setFwcqz(String fwcqz) {
		this.fwcqz = fwcqz;
	}

	public String getFwcb() {
		return fwcb;
	}

	public void setFwcb(String fwcb) {
		this.fwcb = fwcb;
	}

	public Double getFwjsh() {
		return fwjsh;
	}

	public void setFwjsh(Double fwjsh) {
		this.fwjsh = fwjsh;
	}

	public Double getJzmjh() {
		return jzmjh;
	}

	public void setJzmjh(Double jzmjh) {
		this.jzmjh = jzmjh;
	}

	public String getTdsyzh() {
		return tdsyzh;
	}

	public void setTdsyzh(String tdsyzh) {
		this.tdsyzh = tdsyzh;
	}

	public Double getTdsymj() {
		return tdsymj;
	}

	public void setTdsymj(Double tdsymj) {
		this.tdsymj = tdsymj;
	}

	public String getQllx() {
		return qllx;
	}

	public void setQllx(String qllx) {
		this.qllx = qllx;
	}

	public String getQlxz() {
		return qlxz;
	}

	public void setQlxz(String qlxz) {
		this.qlxz = qlxz;
	}

	public String getBz() {
		return bz;
	}

	public void setBz(String bz) {
		this.bz = bz;
	}
}
